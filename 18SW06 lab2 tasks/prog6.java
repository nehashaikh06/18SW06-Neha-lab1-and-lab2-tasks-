class Prog6{
public static void main(String[]args){
int r=4;
float pi=3.14f;
float diam=2*r;
float area=pi*r*r;
float circum=2*pi*r;
System.out.println("Diameter:"+diam);
System.out.println("Area:"+area);
System.out.println("Circumference:"+circum);
}
}                                                                                                                                                                                                                                                                                               
